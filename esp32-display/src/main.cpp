// ---------------------------------------------------------------------------
// Fetch display buffer from server via HTTP
// ---------------------------------------------------------------------------
static bool fetchDisplayBuffer(const String &serverUrl, uint8_t *buf) {
    HTTPClient http;
    String url = serverUrl;

    // Ensure URL ends with /display
    if (!url.endsWith("/display")) {
        if (!url.endsWith("/")) {
            url += "/";
        }
        url += "display";
    }

    Serial.printf("Fetching: %s\n", url.c_str());

    http.begin(url);
    http.setTimeout(30000);  // 30s timeout for large payload

    int httpCode = http.GET();
    if (httpCode != 200) {
        Serial.printf("HTTP error: %d\n", httpCode);
        http.end();
        return false;
    }

    int contentLength = http.getSize();
    Serial.printf("Content-Length: %d\n", contentLength);

    // Stream the response directly into the PSRAM buffer
    WiFiClient *stream = http.getStreamPtr();
    uint32_t bytesRead = 0;
    uint32_t targetSize = DISPLAY_BUFFER_SIZE;

    while (bytesRead < targetSize && http.connected()) {
        size_t available = stream->available();
        if (available > 0) {
            size_t toRead = min((size_t)(targetSize - bytesRead), available);
            size_t got = stream->readBytes(buf + bytesRead, toRead);
            bytesRead += got;

            // Progress every 10%
            if (bytesRead % (targetSize / 10) < got) {
                Serial.printf("  %lu / %lu bytes\n", (unsigned long)bytesRead, (unsigned long)targetSize);
            }
        } else {
            delay(10);
        }
    }

    http.end();

    Serial.printf("Received %lu bytes\n", (unsigned long)bytesRead);
    if (bytesRead != targetSize) {
        Serial.printf("ERROR: expected %lu bytes, got %lu\n",
                       (unsigned long)targetSize, (unsigned long)bytesRead);
        return false;
    }

    return true;
}

// ---------------------------------------------------------------------------
// Arduino entry points
// ---------------------------------------------------------------------------

void setup() {
    Serial.begin(115200);
    delay(500);
    Serial.println("\n=== Linky Dashboard ESP32 ===");

    // Initialize GPIO pins for the display
    DEV_Module_Init();

    // Try to load saved WiFi/server config
    String ssid, pass, serverUrl;
    bool hasConfig = loadConfig(ssid, pass, serverUrl);

    if (!hasConfig) {
        // ----- FIRST BOOT: AP + SETUP SCREEN -----
        Serial.println("No config found. Starting setup mode...");

        // Show setup instructions on e-paper
        uint8_t *imageBuffer = (uint8_t *)ps_malloc(DISPLAY_BUFFER_SIZE);
        if (imageBuffer) {
            EPD_10in85g_Init();
            drawSetupScreen(imageBuffer);
            EPD_10in85g_Display(imageBuffer);
            EPD_10in85g_Sleep();
            free(imageBuffer);
            Serial.println("Setup screen displayed.");
        } else {
            Serial.println("ERROR: Failed to allocate PSRAM for setup screen!");
            // Still start portal even without the screen
        }

        // Start captive portal (does not block)
        startPortal();
        Serial.println("Portal running. Waiting for config...");
        return;  // loop() will handle DNS processing
    }

    // ----- NORMAL BOOT: FETCH + DISPLAY + SLEEP -----
    Serial.printf("Config loaded: SSID=%s, Server=%s\n", ssid.c_str(), serverUrl.c_str());

    // Connect to WiFi with retries
    bool connected = false;
    for (int attempt = 1; attempt <= WIFI_MAX_RETRIES; attempt++) {
        Serial.printf("WiFi connect attempt %d/%d...\n", attempt, WIFI_MAX_RETRIES);

        WiFi.mode(WIFI_STA);
        WiFi.begin(ssid.c_str(), pass.c_str());

        unsigned long startMs = millis();
        while (WiFi.status() != WL_CONNECTED && (millis() - startMs) < WIFI_TIMEOUT_MS) {
            delay(250);
            Serial.print(".");
        }
        Serial.println();

        if (WiFi.status() == WL_CONNECTED) {
            connected = true;
            Serial.printf("Connected! IP: %s\n", WiFi.localIP().toString().c_str());
            break;
        }

        Serial.println("WiFi connection failed.");
        WiFi.disconnect(true);
        delay(1000);
    }

    if (!connected) {
        Serial.println("All WiFi attempts failed. Clearing config and rebooting...");
        clearConfig();  // Will restart into AP mode
        return;         // Should not reach here
    }

    // Allocate display buffer in PSRAM
    uint8_t *imageBuffer = (uint8_t *)ps_malloc(DISPLAY_BUFFER_SIZE);
    if (!imageBuffer) {
        Serial.println("ERROR: Failed to allocate PSRAM buffer!");
        // Sleep and try again later
        esp_deep_sleep(DEEP_SLEEP_SECONDS * 1000000ULL);
        return;
    }

    // Fetch the rendered display from the server
    bool success = fetchDisplayBuffer(serverUrl, imageBuffer);
    if (!success) {
        Serial.println("Failed to fetch display buffer. Sleeping...");
        free(imageBuffer);
        WiFi.disconnect(true);
        esp_deep_sleep(DEEP_SLEEP_SECONDS * 1000000ULL);
        return;
    }

    // Push buffer to the e-paper display
    Serial.println("Initializing display...");
    EPD_10in85g_Init();

    Serial.println("Rendering...");
    EPD_10in85g_Display(imageBuffer);

    Serial.println("Display going to sleep...");
    EPD_10in85g_Sleep();

    // Cleanup
    free(imageBuffer);
    WiFi.disconnect(true);
    DEV_Module_Exit();

    // Deep sleep for 1 hour
    Serial.printf("Entering deep sleep for %d seconds...\n", DEEP_SLEEP_SECONDS);
    esp_deep_sleep(DEEP_SLEEP_SECONDS * 1000000ULL);
}

void loop() {
    // This loop only runs in portal mode (setup).
    // In normal mode, setup() ends with deep sleep.
    portalLoop();
    delay(10);
}
