// ---------------------------------------------------------------------------
// Pixel drawing into the 2-bit-per-pixel display buffer
// Buffer layout: 1360 pixels wide / 4 pixels per byte = 340 bytes per row
// Total: 340 * 480 = 163,200 bytes
// Each byte: [px0(7:6)] [px1(5:4)] [px2(3:2)] [px3(1:0)]
// ---------------------------------------------------------------------------

static const uint16_t ROW_BYTES = 340;  // 1360 / 4

/**
 * Set a single pixel in the buffer.
 * x: 0..1359, y: 0..479
 * color: 0=black, 1=white, 2=yellow, 3=red
 */
static void setPixel(uint8_t *buf, uint16_t x, uint16_t y, uint8_t color) {
    if (x >= 1360 || y >= 480) return;

    uint32_t byteIndex = (uint32_t)y * ROW_BYTES + (x / 4);
    uint8_t bitPos = 6 - (x % 4) * 2;  // px0=bits7:6, px1=bits5:4, etc.

    buf[byteIndex] &= ~(0x03 << bitPos);       // clear the 2 bits
    buf[byteIndex] |= ((color & 0x03) << bitPos); // set new color
}

/**
 * Draw a single character at pixel position (x, y) using the 8x16 font.
 * scale: integer multiplier (1 = 8x16, 2 = 16x32, etc.)
 */
static void drawChar(uint8_t *buf, uint16_t x, uint16_t y, char c, uint8_t color, uint8_t scale) {
    if (c < 32 || c > 127) c = '?';
    uint8_t idx = c - 32;

    for (uint8_t row = 0; row < 16; row++) {
        uint8_t rowData = pgm_read_byte(&FONT_8x16[idx][row]);
        for (uint8_t col = 0; col < 8; col++) {
            if (rowData & (0x80 >> col)) {
                // Draw a scale x scale block for this pixel
                for (uint8_t sy = 0; sy < scale; sy++) {
                    for (uint8_t sx = 0; sx < scale; sx++) {
                        setPixel(buf, x + col * scale + sx, y + row * scale + sy, color);
                    }
                }
            }
        }
    }
}

